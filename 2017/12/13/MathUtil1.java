/**
 * 206：反三角函数的使用（Math 类的使用）
 */

public class MathUtil1 {
    public static void main(String[] args) {
        System.out.println("30° 的反正弦值：" + Math.asin(Math.PI / 6));
        System.out.println("30° 的反余弦值：" + Math.acos(Math.PI / 6));
        System.out.println("30° 的反正切值：" + Math.atan(Math.PI / 6));
    }
}
